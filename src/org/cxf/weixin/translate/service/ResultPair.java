package org.cxf.weixin.translate.service;

public class ResultPair {
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}
	public String getDst() {
		return dst;
	}
	public void setDst(String dst) {
		this.dst = dst;
	}
	private String src;
	private String dst;	
}
